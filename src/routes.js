const { addBookHandler } = require('./handler/post');
const { getAllBooksHandler, getBookByIdHandler } = require('./handler/get');
const { editBookByIdHandler } = require('./handler/put');
const { deleteBookByIdHandler } = require('./handler/delete');

const routes = [
  {
    method: 'POST',
    path: '/books',
    handler: addBookHandler,
  },
  {
    method: 'GET',
    path: '/books',
    handler: getAllBooksHandler,
  },
  {
    method: 'GET',
    path: '/books/{id}',
    handler: getBookByIdHandler,
  },
  {
    method: 'PUT',
    path: '/books/{id}',
    handler: editBookByIdHandler,
  },
  {
    method: 'DELETE',
    path: '/books/{id}',
    handler: deleteBookByIdHandler,
  },
];

module.exports = routes;
